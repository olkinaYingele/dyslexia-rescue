// Нормализует разные виды кавычек к стандартным ивритским символам גרשיים/גרש.
// Это нужно чтобы потом наш словарь сокращений сработал (Gemini может вернуть ASCII " вместо ״).
export function normalizeHebrewQuotes(text: string): string {
  return text.replace(/["""]/g, '״').replace(/[''']/g, '׳');
}
